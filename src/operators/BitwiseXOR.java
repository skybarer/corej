package operators;

public class BitwiseXOR {
	public static void main(String args[]) {

		int num1 = 1;
		int num2 = 1;

		System.out.println("XOR Result =" + (num1 ^ num2));

		num1 = 1;
		num2 = 0;

		System.out.println("XOR Result =" + (num1 ^ num2));

		num1 = 0;
		num2 = 1;

		System.out.println("XOR Result =" + (num1 ^ num2));

		num1 = 0;
		num2 = 0;

		System.out.println("XOR Result =" + (num1 ^ num2));

	}

}
